package TenSix;

public class Account {

	String name;
	int accountNumber;
	int accountBalance;
	int deposit;

	String getName(String name)	{
		return name;
	}
	int getNo(int accountNumber)	{
		return accountNumber;
	}
	int getBalance(int accountBalance)	{
		return accountBalance;
	}
	int deposit(int deposit)	{
		return deposit;
	}
	
}
